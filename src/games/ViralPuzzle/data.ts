// Функція для отримання випадкової головоломки, уникаючи повторень
export const getRandomPuzzle = (excludeId?: number): Puzzle => {
  const availablePuzzles = puzzles.filter((p) => p.id !== excludeId);
  const randomIndex = Math.floor(Math.random() * availablePuzzles.length);
  return availablePuzzles[randomIndex];
};
